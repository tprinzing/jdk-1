package jdk.internal.event;


import java.net.InetAddress;
import java.net.InetSocketAddress;
import java.net.SocketAddress;
import java.net.UnixDomainSocketAddress;

public class SocketWriteEvent extends Event {

    //@Label("Remote Host")
    public String host;

    //@Label("Remote Address")
    public String address;

    //@Label("Remote Port")
    public int port;

    //@Label("Bytes Written")
    //@Description("Number of bytes written to the socket")
    //@DataAmount
    public long bytesWritten;

    /**
     * Actually commit a socket write event.  This is generated automatically.
     * The order of the fields must be the same as the parameters in in this method.
     * {@code commit(..., String, String, int, long)}
     * @param start
     * @param duration
     * @param host
     * @param address
     * @param port
     * @param bytes
     */
    public static void commit(long start, long duration, String host, String address, int port, long bytes) {
        // Generated by JFR
    }

    public static boolean shouldCommit(long duration) {
        // Generated by JFR
        return false;
    }

    public static boolean enabled() {
        // Generated by JFR
        return false;
    }

    public static long timestamp() {
        // Generated by JFR
        return 0L;
    }

    /**
     * Execute the standard boilerplate that proceeds a potential call to the machine generated
     * commit method.
     *
     * @param start  the start time
     * @param bytesWritten  how many bytes were sent
     * @param remote  the address of the remote socket being written to
     */
    public static void checkForCommit(long start, long bytesWritten, SocketAddress remote, Throwable thrown) {
        long duration = timestamp() - start;
        if (shouldCommit(duration)) {
            long bytes = bytesWritten < 0 ? 0 : bytesWritten;
            if (remote instanceof InetSocketAddress isa) {
                InetAddress a = isa.getAddress();
                commit(start, duration, a.getHostName(), a.getHostAddress(), isa.getPort(), bytes);
            } else if (remote instanceof UnixDomainSocketAddress) {
                UnixDomainSocketAddress udsa = (UnixDomainSocketAddress) remote;
                String path = "[" + udsa.getPath().toString() + "]";
                commit(start, duration, "Unix domain socket", path, 0, bytes);
            }
        }
    }
}
